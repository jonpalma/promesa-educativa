					<br><br>
					En el 2013 Promesa Educativa para México, A.C. da inicio a su segundo programa de acción: el Colegio Riberas, una escuela primaria de horario extendido con sentido social en la Colonia Riberas de Sacramento, atendiendo actualmente a 180 niños de 1º a 3er. grado . Nuestra meta es lograr que cada uno de los alumnos llegue a la universidad, beneficiándolos con un programa educativo inspirado en KIPP (Knowledge is Power Program) orientado tanto a la formación académica de excelencia, como a la formación del carácter. El Colegio Riberas forma parte de The One World Network of Schools, una red de instituciones educativas inspiradas en KIPP en el mundo. Mediante una educación de calidad, inclusiva e innovadora, el Colegio Riberas busca desarrollar  al máximo las capacidades de nuestros alumnos para que se conviertan en personas exitosas tanto en lo personal como en lo profesional, contribuyendo también a que logren un cambio positivo en su comunidad.
					<br><br>
					En Promesa Educativa para México, A.C. creemos que la suma de esfuerzos es fundamental para lograr los objetivos de nuestros programas, por lo que trabajamos a través de alianzas de colaboración con asociaciones civiles, diferentes órdenes de gobierno e iniciativa privada; ello ha permitido que nuestros programas se fortalezcan y crezcan, incrementado el número de beneficiarios directos e indirectos.
